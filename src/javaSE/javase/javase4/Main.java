package javaSE.javase.javase4;

/**
 * 整数类型
 * 整数类型是最容易理解的类型 既然我们知道了计算机中的二进制数字是如何表示的 那么我们就可以很轻松的以二进制的形式来表达我们十进制的内容了
 *
 * 在java中 整数类型包括以下几个:
 *      > byte字节型(8个bit 也就是1个字节)范围: -128 ~ +127
 *      > short短整型(16个bit 也就是2个字节)范围: -32768 ~ +32767
 *      > int整型(32个bit 也就是4个字节) 最常用的类型
 *      > long长整型(64个bit 也就是8个字节) 最后需要添加l或L
 *
 * long都装不下怎么办 BigInteger
 *
 * 数字已经达到byte的最大值了 还能加吗 为了便于理解 以4bit为例:
 *                      0111 + 0001 = 1000 => -8(你没有看错 就是这样)
 *
 * 整数还能使用8进制 16进制表示:
 *      > 十进制为15 = 八进制表示为017 = 十六进制表示为0xF = 二进制表示1111 (代码里面不能使用二进制)
 *
 * 字符串型和字符串
 * 在java中 存在字符类型 它能够代表一个字符:
 *      > char字符型(16个bit 也就是2字节 它不带符号)范围是: 0~ 65535
 *      > 使用Unicode表示就是: \u0000 ~ \uffff
 *
 * 字符要用单引号扩起来 比如char c = '淦';
 *
 * 字符其实本质也是数字 但是这些数字通过编码表进行映射 代表了不同的字符 比如字符'A'的ASCII码就是数字65 所以 char类型其实可以转换为上面的整数类型
 *
 * java的char采用Unicode编码表(不是ASCII编码) Unicode编码表包含ASCII的所有内容 同时还包含了全世界的语言 ASCII只有1字节 而Unicode编码是2字节
 * 能够代表65536种文字 足以包含全世界的文字了(我们编译出来的字节码文件也是使用Unicode编码的 所以利用这种特性 其实java支持中文变量名称 方法名称甚至是类名)
 *
 * 既然char是能代表一个字符 那怎么才能包含一句话呢(关于数组 我们这里先不了解 数组我们放在面向对象章节讲解)
 *
 * String就是java中的字符串类型(注意: 它是一个类 创建出来的字符串本质是一个对象 不是我们的基本类型) 字符串就像它的名字一样 代表一串字符 也就是一句完整的话
 *
 * 字符串用双引号括起来 比如: String str = "一日三餐没烦恼";
 */
public class Main {

    public static void main(String[] args) {

        byte b = 127;
        System.out.println((byte) (b + 1));

        String str = "yxsnb";
        System.out.println(str);

    }

}
